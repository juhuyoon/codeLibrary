# Comparison Operators

# In python, it's 2 == as one equal means that you are assigning values.
print(2 == 2)

print (3 != 3)


#Chaining Comparison Operators with Logical Operators
#Use "and" "or" "not" to chain comparisons

#Simple comparisons
print(1 < 2 < 3)

#Logical Operator
print(('h' == 'h') and(2 == 2) )

print(100 == 1 or 2 == 2)

#Not logical operator, which is ASKING FOR THE OPPOSITE OF WHAT IT IS
print(not(400 < 5000)) #should be true, but result will be false
print(not(1 == 1)) #should be true, result will be false

